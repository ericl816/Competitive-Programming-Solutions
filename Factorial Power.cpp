// Use Kamenetsky's formula: f(x) = n* log10(( n/ e)) + log10(2*pi*n)/2 
inline ll Calc (ll x) {
	if (x < 0LL) return 0LL;
	if (x <= 1LL) return 1LL;
	long double res = ((x * log10(x / M_E) + log10(2 * M_PI * x) / 2.0)); 
	return (ll) floor(res) + 1;
}

