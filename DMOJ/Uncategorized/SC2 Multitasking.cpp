int N, ans;

string a = "qwertyuiop", b = "asdfghjkl", c = "zxcvbnm";

inline bool Ok (string s) {
	bool flag1 = 1, flag2 = 1, flag3 = 1;
	for (char ch : s) flag1 &= a.find(ch) != string::npos;
	for (char ch : s) flag2 &= b.find(ch) != string::npos;
	for (char ch : s) flag3 &= c.find(ch) != string::npos;
	return flag1 || flag2 || flag3;
}

